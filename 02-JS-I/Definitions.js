//Variables: Son declaraciones (letra, palabra, caracteres, etc.) que guardan un valor asignado
// que se puede usar en cualquier otro momento.

//Strings: Son cadenas de texto (letras, numeros, caracteres). En el caso de numeros
//no se pueden hacer operaciones matemáticas con ellos.
// Funciones: Son instrucciones que ejecuta el computador a partir de unos parametros iniciales
//  y que se pueden invocar en cualquier momento.
//Declaraciones (if): Son condiciones o preguntas que se hace el programa y que debe tener
//una respuesta como SI o NO (Verdadero o Falso) y segun la respuesta que arroje, ejecuta una acción.
// Valores Booleanos: Son respuestas lógicas a una pregunta que solo puede tener dos opciones
// verdadero o falso, SI o NO.